    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Detalle detalle = new Detalle();
        String texto = "";
        if (jButtonCalcular.isEnabled()) {
            texto = "  **************   MATRIZ INICIAL   ***************" + "\n";
            for (Object object : simplex.getDetalles()) {
                texto += object;
            }
            detalle.getjTextAreaDetalles().setText(texto);
            jDesktopPane1.add(detalle);
            detalle.show();
        } else {
            texto=""
                    + "INSTRUCCIONES+\n"
                    + "1.- Al inicio, necesariamente hay que ingresar la función objetvio, eje.\n"
                    + "z=#x#+#x#+#xn \n"
                    + "z=3x1+5x2 \n"
                    + "2.- Se ingresan las inecuaciones, para representar a (menor igual) se utiliza (<) ejem.  \n"
                    + "#x#+#x#<# \n"
                    + "3x1<8   :  tres equis uno menor que ocho";
            detalle.getjTextAreaDetalles().setText(texto);
            jDesktopPane1.add(detalle);
            detalle.show();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

