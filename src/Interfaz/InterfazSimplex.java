    private void jButtonAnadirFuncionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnadirFuncionActionPerformed
        // TODO add your handling code here:
String areaTexto = jTextAreaFunciones.getText();
        try {
            String cadena = jTextFieldFuncion.getText();
            AnalizadorCadenas analizador = new AnalizadorCadenas();
            if (jCheckBoxFunObj.isSelected()) {
                analizador.analizador(cadena);
                gmatriz.anadirFuncion(analizador);
                contEcuac++;
                crearNombresColumna();
                if (contEcuac >= (gmatriz.getMatriz()[0].length) / 2 - 1) {
                    jCheckBoxValEcua.setSelected(true);
                    jLabelMensajes.setText("LISTO PARA CALCULAR");
                    jButtonCalcular.setEnabled(true);
                    llenarTabla();
                }
                areaTexto += "Ecuación " + contEcuac + " :\t" + cadena + "\n";
                jLabelMensajes.setText("INGRESE ECUACIONES..");
            } else {
                analizador.analizador(cadena);
                areaTexto += "Función Objetivo: \t" + cadena + "\n";
                gmatriz.generarMatriz(analizador);
                jCheckBoxFunObj.setSelected(true);
                jLabelMensajes.setText("INGRESE ECUACIONES..");
                crearNombresColumna();
            }
            jTextAreaFunciones.setText(areaTexto);
        } catch (Exception e) {
            System.out.println("XXXXXXXX");
        }
    }//GEN-LAST:event_jButtonAnadirFuncionActionPerformed

    private void crearNombresColumna() {
        //crear los nombres de columna
        nombres = new Object[gmatriz.getMatriz()[0].length];
        //Obtner Variables de decisión 
        int varDes = gmatriz.getNrovaribles();
        //Añadir Z
        nombres[0] = "Z";
        //Añadir R
        nombres[nombres.length - 1] = "R";
        //Anadir X
        for (int i = 1; i <= varDes; i++) {
            nombres[i] = "X" + i;
        }
        //Añadir H
        for (int i = varDes + 1, k = 1; i < nombres.length - 1; i++, k++) {
            nombres[i] = "H" + k;
        }
    }
    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        // TODO add your handling code here:
        simplex = new Simplex(gmatriz.getMatriz());
        int n = 1;
        while (!simplex.varDesicionCero(n)) {
            detalles += simplex.imprimirMatriz();
            n++;
        }
        jTextAreaRespuesta.setText(simplex.imprimirRespuesta());
    }//GEN-LAST:event_jButtonCalcularActionPerformed

    private void llenarTabla() {
        int tamanio = gmatriz.getMatriz()[0].length;
        Object[][] datos = new Object[gmatriz.getMatriz().length][tamanio];
        for (int i = 0; i < gmatriz.getMatriz().length; i++) {
            for (int j = 0; j < tamanio; j++) {
                datos[i][j] = gmatriz.getMatriz()[i][j];
            }
        }
        TableModel tm = new DefaultTableModel(datos, nombres);
        tablaMuestraDatos.setModel(tm);
    }
